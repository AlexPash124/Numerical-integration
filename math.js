
// trigonometry

const sin = x => Math.sin(x);
const cos = x => Math.cos(x);
const tg = x => Math.tan(x);
const ctg = x => (1 / Math.tan(x));

// pow
const pow = (x, n) =>  Math.pow(x, n);
const nrt = (x, n) =>  Math.pow(x, 1 / n);
const log = (x, n) => Math.log(n , x);
const e = (x) =>  Math.exp(x);
const log1p = (x) => Math.log1p(x);
